"""
 Програмний розрахунок площі трикутника
"""

try:
    a = float(input("довжина основи a = "))
    h = float(input("висота h = "))

except ValueError:
    print("Помилка: введіть числові значення!")
else:
    if a <= 0 or h <= 0:
        print("довжина основи та висота не можуть бути меньше або дорівнювати 0")
    else:
        S = (a * h) / 2
        print(f"Площа трикутника S = {round(S, 2)}")
finally:
    print("Програма завершена ")
